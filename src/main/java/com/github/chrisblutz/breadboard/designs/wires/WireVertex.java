package com.github.chrisblutz.breadboard.designs.wires;

public class WireVertex {

    private int x;
    private int y;

    public WireVertex() {}

    public WireVertex(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
