package com.github.chrisblutz.breadboard.utils;

public class Vertex {

    public int x;
    public int y;

    public Vertex() {}

    public Vertex(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
